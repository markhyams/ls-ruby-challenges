class InvalidCodonError < StandardError; end

module Translation
  TRANSLATE_TABLE = {
    'AUG' => 'Methionine',
    'UUU' => 'Phenylalanine',
    'UUC' => 'Phenylalanine',
    'UUA' => 'Leucine',
    'UUG' => 'Leucine',
    'UCU' => 'Serine',
    'UCC' => 'Serine',
    'UCA' => 'Serine',
    'UCG' => 'Serine',
    'UAU' => 'Tyrosine',
    'UAC' => 'Tyrosine',
    'UGU' => 'Cysteine',
    'UGC' => 'Cysteine',
    'UGG' => 'Tryptophan',
    'UAA' => 'STOP',
    'UAG' => 'STOP',
    'UGA' => 'STOP'
  }

  def self.of_codon(codon)
    TRANSLATE_TABLE.fetch(codon) { raise InvalidCodonError }
  end

  def self.of_rna(strand)
    acids = strand.scan(/.../).map { |codon| of_codon(codon) }
    acids.take_while { |acid| acid != 'STOP' }
  end
end
